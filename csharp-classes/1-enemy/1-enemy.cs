namespace Enemies
{
	/// <summary>
	/// Represents a Zombie enemy.
	/// </summary>
	public class Zombie
	{
		/// <summary>
		/// Health of the Zombie.
		/// </summary>
		public int health;

		/// <summary>
		/// Initializes a new instance of the <see cref="Zombie"/> class with health set to 0.
		/// </summary>
		public Zombie()
		{
			health = 0;
		}
	}
}
